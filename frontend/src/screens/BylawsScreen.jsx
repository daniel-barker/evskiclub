      <p>
        The Membership Committee shall be chaired by the Vice President and
        shall be responsible for promulgation of rules and procedures to be
        employed by the Club in the application process for new General Members,
        for distributing applications for membership, for receiving and
        reviewing such applications for accuracy and completeness, and for
        presenting the applications for General Membership to the Board of
        Directors for their consideration.
      </p>

